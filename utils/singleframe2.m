% Create singleframe2
function O=singleframe2(I)
[s1,s2]=size(I);


O=zeros(s1/5,s2);
O(:,1)=I(s1/5*2+1:s1/5*3,1);
O(:,2)=I(s1/5*2+1:s1/5*3,2);
O(:,end-1)=I(s1/5*2+1:s1/5*3,end-1);
O(:,end)=I(s1/5*2+1:s1/5*3,end);
for i=3:s2-2
    O(:,i)=(I(s1/5*4+1:end,i-2)+I(s1/5*3+1:s1/5*4,i-1)+I(s1/5*2+1:s1/5*3,i)+I(s1/5+1:s1/5*2,i+1)+I(1:s1/5,i+2))/5;
    %O(:,i)=(I(s1/5*4+1:end,i-2).*I(s1/5*3+1:s1/5*4,i-1).*I(s1/5*2+1:s1/5*3,i).*I(s1/5+1:s1/5*2,i+1).*I(1:s1/5,i+2)).^(1/5);
    %O(:,i)=I(s1/5*2+1:s1/5*3,i);
    %O(:,i)=max([I(s1/5*4+1:end,i-2),I(s1/5*3+1:s1/5*4,i-1),I(s1/5*2+1:s1/5*3,i),I(s1/5+1:s1/5*2,i+1),I(1:s1/5,i+2)],[],2);
    %O(:,i)=min([I(s1/5*4+1:end,i-2),I(s1/5*3+1:s1/5*4,i-1),I(s1/5*2+1:s1/5*3,i),I(s1/5+1:s1/5*2,i+1),I(1:s1/5,i+2)],[],2);
    %O(:,i)=median([I(s1/5*4+1:end,i-2),I(s1/5*3+1:s1/5*4,i-1),I(s1/5*2+1:s1/5*3,i),I(s1/5+1:s1/5*2,i+1),I(1:s1/5,i+2)],2);
    
end
